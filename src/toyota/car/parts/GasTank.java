package toyota.car.parts;

public class GasTank {
    private int amountOfGas;

    public GasTank(int amountOfGas) {
        this.amountOfGas = amountOfGas;
    }

    public int getAmountOfGas() {
        return amountOfGas;
    }

    public void setAmountOfGas(int amountOfGas) {
        this.amountOfGas = amountOfGas;
    }
}
